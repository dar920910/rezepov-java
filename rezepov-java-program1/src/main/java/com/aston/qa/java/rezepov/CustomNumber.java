package com.aston.qa.java.rezepov;

public class CustomNumber {
    private final String name;
    private final int value;

    public CustomNumber(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }
}
